/* 
    - Expressions
    - Operators
        Binary
        Unary
        Ternary
*/

let number = 1;

// binary, pois são dois valores e há um operador entre eles
console.log(number + 1)

// unary, pois há um elemento
console.log(typeof number)
console.log(++number)

// ternary, tres expressoes para definir algo no final
console.log(true ? 'ola' : 'nada')